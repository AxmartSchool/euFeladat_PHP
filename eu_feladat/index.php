<h1>Europai Unio</h1>

<img src="./forras/unio.jpg" alt="terkep" class="unioTerkep">

<p>Az EU nem volt mindig akkora, mint most. Amikor az európai országok gazdasági együttműködése 1951-ben megkezdődött, abban még csak Belgium, Németország, Franciaország, Olaszország, Luxemburg és Hollandia vett részt.</p>

<p>Idővel egyre több ország döntött a csatlakozás melletten. Az EU jelenleg 27 tagállamból áll. Az Egyesült Királyság 2020. január 31-én kilépett az Európai Unióból.

<h2>Csatlakozás az Európai Unióhoz</h2>

<p>Az EU-hoz történő csatlakozás nem egyik napról a másikra történik: összetett folyamatról van szó. A tagság feltételeit teljesítő, csatlakozni kívánó országnak valamennyi területen alkalmaznia kell az uniós jogszabályokat.</p>

<p>Mindazok az országok a felvételüket kérhetik az EU-ba, amelyek teljesítik a tagság feltételeiten. Ezen feltételek, vagyis az ún. koppenhágai kritériumok közé tartozik a szabad piacgazdaság, stabil demokrácia és jogállamiság megléte, valamint hogy a csatlakozni kívánó ország elfogadja az uniós jogszabályokat, ideértve az euróra vonatkozó feltételeket is.</p>

<p>A csatlakozni kívánó országnak be kell nyújtania csatlakozási kérelmét a Tanácshoz. A Tanács ezután felkéri a Bizottságot, hogy értékelje ki, milyen mértékben tudja teljesíteni az adott ország a koppenhágai kritériumokat. Kedvező vélemény esetén a Tanácsnak döntést kell hoznia tárgyalási meghatalmazás kiadásáról. Ezután hivatalosan is megkezdődnek a tárgyalásoken, melyek során külön fejezetekbe rendezve vitatják meg a különböző témákat.</p>

<p>A tárgyalások időigényesek, mert a tagjelölt országoknak át kell ültetniük nemzeti jogrendjükbe az uniós jogszabályok sokaságát. A csatlakozás előtti időszaken alatt az EU pénzügyien, adminisztratív és technikaien segítséget nyújt ezeknek az országoknak.</p>

<p>Magyarország 2004. május 1. óta tajga az Európai Uniónak.</p>

